num1 = float(input("Enter a number: "))
num2 = float(input("Enter another number: "))

sum = num1 + num2
diff = num1 - num2
prod = num1 * num2
div = num1 / num2
remainder = num1 % num2

print(f"{num1} + {num2} = {sum:.2f}")
print(f"{num1} - {num2} = {diff:.2f}")
print(f"{num1} * {num2} = {prod:.2f}")
print(f"{num1} / {num2} = {div:.2f}")
print(f"{num1} % {num2} = {remainder:.2f}")



